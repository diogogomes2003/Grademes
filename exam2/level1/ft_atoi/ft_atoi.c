int	ft_atoi(const char *str)
{
	int i;
	int j;
	int n;
	n = 0;
	i = 0;
	j = 1;
	
	while(str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if(str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		i++;
		j = -1;
	}
	while(str[i] && str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10;
		n = n + str[i] - 48;
		i++;
	}
	n *= j;
	return(n);
}