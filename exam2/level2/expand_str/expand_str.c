#include <unistd.h>
int ftstr(char *c)
{
	int i = 0;
	while(c[i])
		i++;
	return(i);
}
int main (int ac, char **av)
{
	int i,t = 0;
	if(ac == 2)
	{
		while((av[1][i] == ' ' || av[1][i] == '\t') && av[1][i] != '\0')
			i++;
		while (av[1][i])
		{
			while((av[1][i] == ' ' || av[1][i] == '\t') && av[1][i] != '\0')
			{
				i++;
				if (av[1][i] != '\t' && av[1][i] != ' ' && av[1][i] != '\0' )
					t = 1;
			}
			if (av[1][i - 1] == ' ' && t == 1)
			{
				write(1, "   ", 3);
				t = 0;
			}
			if (av[1][i] != ' ' && av[1][i] != '\0' && av[1][i] != '\t')
			{

				write(1, &av[1][i],1);
				i++;
			}
		}
		
	}
	write(1, "\n", 1);
}